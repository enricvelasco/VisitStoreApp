package com.visitapp.visitstoreapp.sistema.domain.asociaciones;

public class AsociacionParametrosConfig {
    String colorFondoBase;
    String colorToolbar;
    String colorParteSuperiorNavView;
    String colorTextoTitulos;
    String colorTextoContenido;
    String colorTextoPrecios;

    public AsociacionParametrosConfig() {
    }

    public String getColorFondoBase() {
        return colorFondoBase;
    }

    public void setColorFondoBase(String colorFondoBase) {
        this.colorFondoBase = colorFondoBase;
    }

    public String getColorToolbar() {
        return colorToolbar;
    }

    public void setColorToolbar(String colorToolbar) {
        this.colorToolbar = colorToolbar;
    }

    public String getColorParteSuperiorNavView() {
        return colorParteSuperiorNavView;
    }

    public void setColorParteSuperiorNavView(String colorParteSuperiorNavView) {
        this.colorParteSuperiorNavView = colorParteSuperiorNavView;
    }

    public String getColorTextoTitulos() {
        return colorTextoTitulos;
    }

    public void setColorTextoTitulos(String colorTextoTitulos) {
        this.colorTextoTitulos = colorTextoTitulos;
    }

    public String getColorTextoContenido() {
        return colorTextoContenido;
    }

    public void setColorTextoContenido(String colorTextoContenido) {
        this.colorTextoContenido = colorTextoContenido;
    }

    public String getColorTextoPrecios() {
        return colorTextoPrecios;
    }

    public void setColorTextoPrecios(String colorTextoPrecios) {
        this.colorTextoPrecios = colorTextoPrecios;
    }
}
